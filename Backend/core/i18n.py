def _script_safe_fallback_bundle(language: str) -> dict[str, str]:
    """Build a deterministic native-script-safe fallback bundle per language."""
    bundle = dict(_BASE_BUNDLES["en"])
    default_text = _SCRIPT_SAFE_DEFAULT_TEXT.get(language, "स्थानीय भाषा")
    for key in bundle:
        bundle[key] = default_text

    core_overrides = _SCRIPT_SAFE_CORE_TEXT.get(language, {})
    bundle.update(core_overrides)
    return bundle


