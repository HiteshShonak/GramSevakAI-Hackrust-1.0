async def _translate_bundle(language: str) -> dict[str, str]:
    """Generate and cache a UI bundle for one supported app language."""
    if language == "en":
        return _BASE_BUNDLES[language]
    if language == "hry":
        bundle = _build_haryanvi_bundle()
        _TRANSLATION_CACHE[language] = bundle
        return bundle
    if language in _TRANSLATION_CACHE:
        return _TRANSLATION_CACHE[language]

    def _is_script_compliant(bundle: dict[str, str]) -> bool:
        pattern = _SCRIPT_PATTERNS.get(language)
        if not pattern:
            return True
        sample = " ".join(
            str(bundle.get(k, ""))
            for k in (
                "homeTitle",
                "homeSubtitle",
                "settingsTitle",
                "discoveryTitle",
                "scamTitle",
                "quickSchemes",
                "quickScam",
                "quickTracker",
                "quickCsc",
            )
        )
        return bool(pattern.search(sample))

    prompt = (
        "Translate the JSON values below for a rural Indian government-help mobile app.\n"
        "Rules:\n"
        "- Keep the JSON keys exactly unchanged.\n"
        "- Keep text short and mobile-friendly.\n"
        "- Keep brand/product names like GramSevak AI, WhatsApp, CSC, OTP unchanged when appropriate.\n"
        f"{_script_instruction(language)}\n"
        "- NEVER write translated text in Roman letters for non-English languages.\n"
        "- Do not add explanations.\n"
        f"Target language code: {language}\n"
        f"JSON:\n{_BASE_BUNDLES['en']}\n\n"
        "Respond ONLY with valid JSON."
    )
    try:
        raw = await call_llm(prompt)
    except Exception as exc:
        log.warning("i18n translation call failed for %s: %s", language, exc)
        raw = ""
    translated = parse_json_safe(raw)
    if translated and not _is_script_compliant(translated):
        strict_prompt = prompt + "\n\nSTRICT MODE: Output MUST use native script characters."
        try:
            raw = await call_llm(strict_prompt)
        except Exception as exc:
            log.warning("i18n strict translation call failed for %s: %s", language, exc)
            raw = ""
        translated = parse_json_safe(raw)

    if not translated:
        fallback = _script_safe_fallback_bundle(language)
        _TRANSLATION_CACHE[language] = fallback
        return fallback

    merged = dict(_BASE_BUNDLES["en"])
    for key in merged:
        value = translated.get(key)
        if isinstance(value, str) and value.strip():
            merged[key] = value.strip()

    if not _is_script_compliant(merged):
        log.warning("i18n bundle for %s looked romanized; using script-safe fallback", language)
        fallback = _script_safe_fallback_bundle(language)
        _TRANSLATION_CACHE[language] = fallback
        return fallback

    _TRANSLATION_CACHE[language] = merged
    return merged

